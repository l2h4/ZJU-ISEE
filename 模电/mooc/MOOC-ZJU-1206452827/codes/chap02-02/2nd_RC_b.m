% with R1=R2=100, C1=1uF, C2=0.01uF
syms s;
R1=100; R2=100; C1=1e-6; C2=1e-8;

A = ...
    [1/R1, -1/R1, 0, -1; ...
     -1/R1, 1/R1+1/R2+s*C1, -1/R2, 0; ...
     0, -1/R2, 1/R2+s*C2, 0; ...
     1, 0, 0, 0];
B = [0; 0; 0; 1/s];

V = A \ B; V(3)

vt =ilaplace(V(3))

ezplot(vt, [0, 1e-3]); grid on; ylim([0, 1]);